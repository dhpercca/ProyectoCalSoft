    private void jButtonRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRegistrarActionPerformed
        // BOTON GUARDAR:
        int id=Integer.parseInt(jLabel8.getText());
        String cod=jTextField2.getText();
        String ti=jTextField1.getText();
        int can=Integer.parseInt(jTextField3.getText());
        int dis=Integer.parseInt(jTextField4.getText());
        
        String autor=String.valueOf(jComboBox1.getSelectedItem());
        String editorial=String.valueOf(jComboBox4.getSelectedItem());
        String categoria=String.valueOf(jComboBox5.getSelectedItem());
        int au=0;
        for(int i=0;i<a.length;i++)
        {   if(autor==a[i][1])
                au=Integer.parseInt(String.valueOf(a[i][0]));
        }
        int ed=0;
        for(int i=0;i<e.length;i++)
        {   if(editorial==e[i][1])
                ed=Integer.parseInt(String.valueOf(e[i][0]));
        }
        int ca=0;
        for(int i=0;i<c.length;i++)
        {   if(categoria==c[i][1])
                ca=Integer.parseInt(String.valueOf(c[i][0]));
        }
        String canti=String.valueOf(can);
        String dispo=String.valueOf(dis);
        
        if(cod.equals("")||ti.equals("")||canti.equals("")||dispo.equals("")){
            JOptionPane.showMessageDialog(null, "Para guardar un registro debe llenar todos los campos...","ERROR: CAMPOS VACIOS",2);
        }
        else{
            //Instanciamos la clase docente
            libro pr=new libro(id,cod,ti,au,ed,ca,can,dis);
            if(id>0)
                pr.actualizar();
            else
            {   Object[][]reg=pr.buscarXdni();
                if((int)reg[0][0]==0) 
                {   pr.guardar();
                    
                }
                else
                    JOptionPane.showMessageDialog(null, "El CODIGO ya esta registrado, verificar por favor...","ERROR: REGISTRO NO GUARDADO",0);
            }
        }
    }//GEN-LAST:event_jButtonRegistrarActionPerformed

