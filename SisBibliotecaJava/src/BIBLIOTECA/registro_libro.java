    public void crearTabla()
    {   String[] columNames={"ID","CODIGO","TITULO","AUTOR","EDITORIAL","CATEGORIA","CANTIDAD","DISPONIBLE"};
        //final JTable tabla = new JTable(p,columNames);
        final JTable tabla = new JTable(prods,columNames);
        //Creamos el scroll
      	JScrollPane scroll = new JScrollPane(tabla);
	//scroll.setBounds(4, 242, 425, 100);
	scrollPane1.add(scroll);
	scroll.setVisible(true);
        tabla.addMouseListener(new MouseAdapter()
        {   @Override
         	public void mouseClicked(MouseEvent e)
      		{   int fila = tabla.rowAtPoint(e.getPoint());
                    showDatos(fila);
                    //nnn=fila;
                }
        });
    }
    void showDatos(int row)
    {   String au="",ed="",ca="";
        jLabel8.setText(String.valueOf(prods[row][0]));
            jTextField2.setText(String.valueOf(prods[row][1]));
            jTextField1.setText(String.valueOf(prods[row][2]));
            jTextField3.setText(String.valueOf(prods[row][6]));
            jTextField4.setText(String.valueOf(prods[row][7]));
            for(int i=0;i<a.length;i++)
            {   if(prods[row][3]==a[i][0])
                    au=String.valueOf(a[i][1]);
            }
            for(int i=0;i<e.length;i++)
            {   if(prods[row][4]==e[i][0])
                    ed=String.valueOf(e[i][1]);
            }
            for(int i=0;i<c.length;i++)
            {   if(prods[row][5]==c[i][0])
                    ca=String.valueOf(c[i][1]);
            }
            jComboBox1.setSelectedItem(au);
            jComboBox4.setSelectedItem(ed);
            jComboBox5.setSelectedItem(ca);
    } 
