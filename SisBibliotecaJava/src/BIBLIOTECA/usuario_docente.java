    private void jButtonRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRegistrarActionPerformed
        // BOTON GUARDAR:
        int id=Integer.parseInt(jLabel9.getText());
        String dni=jTextField4.getText();
        String nom=jTextField1.getText();
        String ap=jTextField2.getText();
        String dir=jTextField3.getText();
        String nivel=String.valueOf(jComboBox1.getSelectedItem());
        int niv;
        if(nivel.equals("PRIMARIA"))
            niv=1;
        else
            niv=2;
        if(dni.equals("")||nom.equals("")|ap.equals("")||dir.equals("")){
            JOptionPane.showMessageDialog(null, "Para guardar un registro debe llenar todos los campos...","ERROR: CAMPOS VACIOS",2);
        }
        else{
            //Instanciamos la clase docente
            docente pr=new docente(id,dni,nom,ap,dir,niv);
            if(id>0)
                pr.actualizar();
            else
            {   Object[][]reg=pr.buscarXdni();
                if((int)reg[0][0]==0) 
                {   pr.guardar();
                    
                }
                else
                    JOptionPane.showMessageDialog(null, "El DNI ya esta registrado, verificar por favor...","ERROR: REGISTRO NO GUARDADO",0);
            }
        }
        
    }//GEN-LAST:event_jButtonRegistrarActionPerformed

    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        // BOTON BUSCAR DNI:
        String niv;
        String dni=jTextField4.getText();
        docente pr=new docente(0,dni,"","","",0);
        Object[][] p=pr.buscarXdni();
        String sid=String.valueOf(p[0][0]);
        if(!sid.equals("0"))
        {   jLabel9.setText(String.valueOf(p[0][0]));
            jTextField4.setText(String.valueOf(p[0][1]));
            jTextField1.setText(String.valueOf(p[0][2]));
            jTextField2.setText(String.valueOf(p[0][3]));
            jTextField3.setText(String.valueOf(p[0][4]));
            if(p[0][5].equals(1))
                niv="PRIMARIA";
            else
                niv="SECUNDARIA";
            jComboBox1.setSelectedItem(niv);
        }
        else
            JOptionPane.showMessageDialog(null, "El DNI NO esta registrado...","MENSAJE: DNI NO ENCONTRADO",2);
    }//GEN-LAST:event_jToggleButton1ActionPerformed

