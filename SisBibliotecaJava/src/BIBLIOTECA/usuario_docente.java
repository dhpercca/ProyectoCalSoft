    private void jButtonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarActionPerformed
        // BOTON BUSCAR POR CAMPO:
        String campo=(String)jComboBoxBusqueda.getSelectedItem();
        String operador=(String)jComboBoxParametro.getSelectedItem();
        String valor=jTextBuscar.getText();
        docente pr=new docente(0,"","","","",0);
        prods=pr.buscar(campo, operador, valor);
        crearTabla();
    }//GEN-LAST:event_jButtonBuscarActionPerformed
    public void crearTabla()
    {   String[] columNames={"ID","DNI","NOMBRES","APELLIDOS","DIRECCION","NIVEL"};
        //final JTable tabla = new JTable(p,columNames);
        final JTable tabla = new JTable(prods,columNames);
        //Creamos el scroll
      	JScrollPane scroll = new JScrollPane(tabla);
	//scroll.setBounds(4, 242, 425, 100);
	scrollPane1.add(scroll);
	scroll.setVisible(true);
        tabla.addMouseListener(new MouseAdapter()
        {   @Override
         	public void mouseClicked(MouseEvent e)
      		{   int fila = tabla.rowAtPoint(e.getPoint());
                    showDatos(fila);
                    //nnn=fila;
                }
        });
    }
    void showDatos(int row)
    {   String niv;
        jLabel9.setText(String.valueOf(prods[row][0]));
        jTextField4.setText(String.valueOf(prods[row][1]));
        jTextField1.setText(String.valueOf(prods[row][2]));
        jTextField2.setText(String.valueOf(prods[row][3]));
        jTextField3.setText(String.valueOf(prods[row][4]));
        if(prods[row][5].equals(1))
                niv="PRIMARIA";
            else
                niv="SECUNDARIA";
        jComboBox1.setSelectedItem(niv);
    }    
